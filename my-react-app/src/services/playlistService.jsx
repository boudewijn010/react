import SpotifyWebApi from "spotify-web-api-js";

const spotifyApi = new SpotifyWebApi();

const moodGenreMapping = {
  happy: ["pop", "electronic"],
  energetic: ["rock", "hip-hop"],
  relaxed: ["classical", "jazz"],
  romantic: ["pop", "indie"],
  melancholic: ["indie", "rock"],
  workout: ["electronic", "hip-hop"],
};

export const generatePlaylist = async (mood, genres) => {
  const recommendedGenres =
    genres.length > 0 ? genres : moodGenreMapping[mood] || [];

  const tracks = [];
  for (const genre of recommendedGenres) {
    try {
      const response = await spotifyApi.searchTracks(`genre:${genre}`, {
        limit: 50, // Increase the limit to get more tracks
      });
      if (response.tracks && response.tracks.items) {
        tracks.push(...response.tracks.items);
      } else {
        console.error(`No tracks found for genre ${genre}`);
      }
    } catch (error) {
      console.error(`Error fetching tracks for genre ${genre}:`, error);
    }
  }

  if (tracks.length === 0) {
    console.error("No tracks found for the selected mood and genres.");
    return {
      id: "1",
      title: "Mood Mix",
      tracks: [],
    };
  }

  const selectedTracks = tracks.slice(0, 30).map((track) => ({
    id: track.id,
    name: track.name,
    artist: track.artists[0].name,
    genre:
      track.genres && track.genres.length > 0 ? track.genres[0] : "unknown",
    albumCover: track.album.images[0]?.url || "",
  }));

  const playlistTitle = `Mood Mix: ${mood} ${genres.join(", ")}`;

  const playlist = {
    id: "1",
    title: playlistTitle,
    tracks: selectedTracks,
  };

  return playlist;
};

export const savePlaylist = async (playlist) => {
  try {
    const user = await spotifyApi.getMe();
    const newPlaylist = await spotifyApi.createPlaylist(user.id, {
      name: playlist.title,
      description: "Generated by Mood Playlist Generator",
      public: false,
    });

    const trackUris = playlist.tracks.map(
      (track) => `spotify:track:${track.id}`
    );
    await spotifyApi.addTracksToPlaylist(newPlaylist.id, trackUris);

    return { success: true, playlistId: newPlaylist.id };
  } catch (error) {
    console.error("Error saving playlist:", error);
    return { success: false, error: error.message };
  }
};
